#!/usr/bin/env python3
"""Run continuous-feedback strict prior-fractional PageRank wavefront tests.

The fixed K-stage wavefront proves one fused super-step.  This script checks
whether the last stage can stream committed digits back into stage 0 for the
next super-step, while observing stage-wise L1 convergence.
"""

from __future__ import annotations

import pathlib
import re
import subprocess
import sys
from dataclasses import dataclass


ROOT = pathlib.Path(__file__).resolve().parents[1]
ITER = ROOT / "MSDF_iterative_solver"
SRC = ROOT / "MSDF_operator_srcs/MSDF_Operators/MSDF_Operators/MSDF_Operators.srcs/sources_1/new"
REPORT = ITER / "generated/prior_fractional_feedback_eval.md"

ORIG_SOURCES = [
    "DFF.v",
    "full_adder.v",
    "parallel_online_adder_block.v",
    "parallel_online_adder.v",
    "parallel_online_adder_4.v",
    "parallel_online_adder_4_with_obuf.v",
    "vector_append.v",
    "selector.v",
    "append_and_select.v",
    "output_and_update.v",
    "MSDF_MUL_ADD_8.v",
]

COUNTER_RE = re.compile(
    r"COUNTERS prior_feedback K=(?P<k>\d+) "
    r"target_supersteps=(?P<target_supersteps>\d+) "
    r"total=(?P<total>\d+) final_supersteps=(?P<final_supersteps>\d+) "
    r"capture=(?P<capture>\d+) stage_counts=(?P<stage_counts>[0-9a-fA-F]+) "
    r"l1_counts=(?P<l1_counts>[0-9a-fA-F]+) "
    r"feedback_stall=(?P<feedback_stall>\d+) cert_late=(?P<cert_late>\d+) "
    r"converged=(?P<converged>\d+) converged_stage=(?P<converged_stage>\d+) "
    r"hist=(?P<hist>[0-9a-fA-F]+) kill=(?P<kill>\d+) "
    r"overlap=(?P<overlap>[01xXzZ]+)"
)


@dataclass(frozen=True)
class Case:
    name: str
    stages: int
    supersteps: int
    eta: int
    expect_converged: int
    expect_stage: int = 0


def run(cmd: list[str]) -> subprocess.CompletedProcess[str]:
    return subprocess.run(cmd, cwd=ROOT, check=True, capture_output=True, text=True)


def run_case(case: Case) -> dict[str, int | str | float]:
    num_iters = case.stages * case.supersteps
    run([
        sys.executable,
        "MSDF_iterative_solver/make_pagerank_prior_fractional_vectors.py",
        "--num-iters",
        str(num_iters),
    ])
    out = pathlib.Path("/tmp") / f"tb_prior_feedback_k{case.stages}_{case.name}.vvp"
    compile_cmd = [
        "iverilog",
        "-g2012",
        "-I",
        "MSDF_iterative_solver/tb",
        f"-DPRIOR_FEEDBACK_STAGES_VALUE={case.stages}",
        f"-DPRIOR_FEEDBACK_SUPERSTEPS_VALUE={case.supersteps}",
        f"-DPRIOR_FEEDBACK_L1_ETA_VALUE={case.eta}",
        f"-DPRIOR_FEEDBACK_EXPECT_CONVERGED_VALUE={case.expect_converged}",
        f"-DPRIOR_FEEDBACK_EXPECT_STAGE_VALUE={case.expect_stage}",
        "-o",
        str(out),
        *[str(SRC / name) for name in ORIG_SOURCES],
        "MSDF_iterative_solver/rtl/iter_fixed_degree_template_unpack.v",
        "MSDF_iterative_solver/prior_rtl/iter_prior_online_mma8_row_kernel.v",
        "MSDF_iterative_solver/prior_rtl/iter_prior_online_mma8_stream_stage_cluster.v",
        "MSDF_iterative_solver/prior_rtl/iter_prior_online_mma8_global_feedback_top.v",
        "MSDF_iterative_solver/tb/tb_iter_prior_online_mma8_global_feedback_top.v",
    ]
    run(compile_cmd)
    sim = run(["vvp", str(out)])
    match = COUNTER_RE.search(sim.stdout)
    if not match:
        raise RuntimeError(f"missing counter line for {case}\n{sim.stdout}")
    row: dict[str, int | str | float] = {"name": case.name}
    for key, value in match.groupdict().items():
        row[key] = value if key in {"stage_counts", "l1_counts", "hist", "overlap"} else int(value)
    row["cycles_per_superstep"] = int(row["total"]) / max(1, int(row["final_supersteps"])) if int(row["final_supersteps"]) else 0.0
    row["stdout"] = sim.stdout.strip()
    return row


def write_report(rows: list[dict[str, int | str | float]]) -> None:
    REPORT.parent.mkdir(parents=True, exist_ok=True)
    lines = [
        "# Prior-Fractional Feedback Wavefront Evaluation",
        "",
        "This report is generated by `run_prior_fractional_feedback_eval.py`.",
        "It uses only the original `MSDF_MUL_ADD_8` prior operator; the PageRank-specific 4-term experimental core is not part of this path.",
        "",
        "The tested datapath is:",
        "",
        "$$",
        r"x^{(k)} \rightarrow x^{(k+K)} \rightarrow \text{feedback FIFO} \rightarrow x^{(k+2K)}",
        "$$",
        "",
        "Stage-wise L1 certification observes every internal iteration:",
        "",
        "$$",
        r"\|x^{(k+s)}-x^{(k+s-1)}\|_1",
        "$$",
        "",
        "| case | K | target supersteps | total cycles | final supersteps | captured digits | cycles / completed superstep | feedback stall | cert late cycles | converged | converged stage | killed digits | overlap |",
        "| --- | ---: | ---: | ---: | ---: | ---: | ---: | ---: | ---: | ---: | ---: | ---: | --- |",
    ]
    for row in rows:
        lines.append(
            f"| {row['name']} | {row['k']} | {row['target_supersteps']} | "
            f"{row['total']} | {row['final_supersteps']} | {row['capture']} | "
            f"{row['cycles_per_superstep']:.2f} | {row['feedback_stall']} | "
            f"{row['cert_late']} | {row['converged']} | {row['converged_stage']} | "
            f"{row['kill']} | `{row['overlap']}` |"
        )
    lines.extend([
        "",
        "Interpretation:",
        "",
        "- `feedback_stall=0` means the feedback stream did not underflow/overflow in the tested schedule.",
        "- `cert_late_cycles` counts cycles where a completed stage word is being reduced into an L1 delta after digit capture.",
        "- The K=4 continuous case completes two K-stage super-steps in one run, so it validates feedback reuse rather than a single fixed super-step.",
        "- The convergence case uses a relaxed eta to force stage-wise stop detection; it validates `converged_stage`, not final PageRank convergence quality.",
        "",
        "Current comparison anchors:",
        "",
        "| datapath | compute cycles / note |",
        "| --- | ---: |",
        "| fixed P3 K=4 runtime wavefront | 70 |",
        "| P4 timing-clean conventional fractional | 44 |",
        "| standalone K=4 feedback, two supersteps | see table above |",
        "",
        "The feedback checkpoint is still standalone.  It proves continuous digit feedback and stage-wise L1 observation before runtime-shell integration.",
        "",
        "## Raw Outputs",
        "",
    ])
    for row in rows:
        lines.extend([
            f"### {row['name']}",
            "",
            "```text",
            str(row["stdout"]),
            "```",
            "",
        ])
    REPORT.write_text("\n".join(lines), encoding="utf-8")


def main() -> None:
    cases = [
        Case("k2_feedback_two_supersteps", stages=2, supersteps=2, eta=64, expect_converged=0),
        Case("k4_feedback_two_supersteps", stages=4, supersteps=2, eta=64, expect_converged=0),
        Case("k4_stage_l1_stop", stages=4, supersteps=2, eta=700, expect_converged=1, expect_stage=1),
    ]
    rows = [run_case(case) for case in cases]
    write_report(rows)
    for row in rows:
        print(
            f"{row['name']}: K={row['k']} total={row['total']} "
            f"final_supersteps={row['final_supersteps']} stall={row['feedback_stall']} "
            f"converged={row['converged']} stage={row['converged_stage']}"
        )
    print(f"Wrote {REPORT}")


if __name__ == "__main__":
    main()
